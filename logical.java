public class logical {
    public static void main(String[] args) {
        //logical operators
        boolean a = true;
        boolean b = false;
        System.out.println("a && b = " + (a && b));
        System.out.println("a || b = " + (a || b));
        System.out.println("!(a && b) = " + (!(a && b)));        
    }
}
